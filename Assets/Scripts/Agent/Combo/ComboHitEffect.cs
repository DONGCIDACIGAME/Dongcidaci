[System.Serializable]
public class ComboHitEffect
{
    /// <summary>
    /// 效果类型
    /// </summary>
    public int effectType;

    /// <summary>
    /// 效果数值
    /// </summary>
    public int effectValue;

    /// <summary>
    /// hit点攻击范围的宽度
    /// </summary>
    public float hitRectWidth;

    /// <summary>
    /// hit点攻击范围的高度
    /// </summary>
    public float hitRectHeight;

    /// <summary>
    /// hit点攻击范围的中心偏移X
    /// </summary>
    public float hitRectOffsetX;

    /// <summary>
    /// hit点攻击范围的中心偏移Y
    /// </summary>
    public float hitRectOffsetY;
}
